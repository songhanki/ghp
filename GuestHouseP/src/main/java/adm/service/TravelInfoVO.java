package adm.service;

public class TravelInfoVO {
	private int ti_seq;
	private String ti_title;
	private String ti_comment;
	private String ti_wdate;
	private String ti_writer;
	private int ti_view;
	private String ti_img;
	
	public int getTi_seq() {
		return ti_seq;
	}
	public void setTi_seq(int ti_seq) {
		this.ti_seq = ti_seq;
	}
	public String getTi_title() {
		return ti_title;
	}
	public void setTi_title(String ti_title) {
		this.ti_title = ti_title;
	}
	public String getTi_comment() {
		return ti_comment;
	}
	public void setTi_comment(String ti_comment) {
		this.ti_comment = ti_comment;
	}
	public String getTi_wdate() {
		return ti_wdate;
	}
	public void setTi_wdate(String ti_wdate) {
		this.ti_wdate = ti_wdate;
	}
	public String getTi_writer() {
		return ti_writer;
	}
	public void setTi_writer(String ti_writer) {
		this.ti_writer = ti_writer;
	}
	public int getTi_view() {
		return ti_view;
	}
	public void setTi_view(int ti_view) {
		this.ti_view = ti_view;
	}
	public String getTi_img() {
		return ti_img;
	}
	public void setTi_img(String ti_img) {
		this.ti_img = ti_img;
	}
	
	
	
}
