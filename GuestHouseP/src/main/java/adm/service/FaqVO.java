package adm.service;

public class FaqVO {

	private int faq_seq;
	private String faq_title;
	private String faq_comment;
	private String faq_wdate;
	private String faq_category;
	private String faq_writer;
	public int getFaq_seq() {
		return faq_seq;
	}
	public void setFaq_seq(int faq_seq) {
		this.faq_seq = faq_seq;
	}
	public String getFaq_title() {
		return faq_title;
	}
	public void setFaq_title(String faq_title) {
		this.faq_title = faq_title;
	}
	public String getFaq_comment() {
		return faq_comment;
	}
	public void setFaq_comment(String faq_comment) {
		this.faq_comment = faq_comment;
	}
	public String getFaq_wdate() {
		return faq_wdate;
	}
	public void setFaq_wdate(String faq_wdate) {
		this.faq_wdate = faq_wdate;
	}
	public String getFaq_category() {
		return faq_category;
	}
	public void setFaq_category(String faq_category) {
		this.faq_category = faq_category;
	}
	public String getFaq_writer() {
		return faq_writer;
	}
	public void setFaq_writer(String faq_writer) {
		this.faq_writer = faq_writer;
	}
	
	
}
