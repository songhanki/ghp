public class TravelReviewVO {
	private int tr_seq;
	private String tr_title;
	private String tr_comment;
	private String tr_wdate;
	private String tr_writer;
	private int tr_view;
	private String tr_img;
	
	public int getTr_seq() {
		return tr_seq;
	}
	public void setTr_seq(int tr_seq) {
		this.tr_seq = tr_seq;
	}
	public String getTr_title() {
		return tr_title;
	}
	public void setTr_title(String tr_title) {
		this.tr_title = tr_title;
	}
	public String getTr_comment() {
		return tr_comment;
	}
	public void setTr_comment(String tr_comment) {
		this.tr_comment = tr_comment;
	}
	public String getTr_wdate() {
		return tr_wdate;
	}
	public void setTr_wdate(String tr_wdate) {
		this.tr_wdate = tr_wdate;
	}
	public String getTr_writer() {
		return tr_writer;
	}
	public void setTr_writer(String tr_writer) {
		this.tr_writer = tr_writer;
	}
	public int getTr_view() {
		return tr_view;
	}
	public void setTr_view(int tr_view) {
		this.tr_view = tr_view;
	}
	public String getTr_img() {
		return tr_img;
	}
	public void setTr_img(String tr_img) {
		this.tr_img = tr_img;
	}
	
	
	
}
